package itens;

public class PersonagemEmCena extends Personagem {
	private String imagemEmCena;
	private int posicaoX;
	private int posicaoY;
	public String getImagemEmCena() {
		return imagemEmCena;
	}
	public void setImagemEmCena(String imagemEmCena) {
		this.imagemEmCena = imagemEmCena;
	}
	public int getPosicaoX() {
		return posicaoX;
	}
	public void setPosicaoX(int posicaoX) {
		this.posicaoX = posicaoX;
	}
	public int getPosicaoY() {
		return posicaoY;
	}
	public void setPosicaoY(int posicaoY) {
		this.posicaoY = posicaoY;
	}
	
}
